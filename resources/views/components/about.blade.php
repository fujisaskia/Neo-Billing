<script>
    document.querySelectorAll('.accordion-button').forEach(button => {
        button.addEventListener('click', () => {
            const content = document.querySelector(button.dataset.target);
            const expanded = button.getAttribute('aria-expanded') === 'true';

            // Close all other accordion contents
            document.querySelectorAll('.accordion-content').forEach(otherContent => {
                if (otherContent !== content) {
                    otherContent.style.maxHeight = 0;
                    otherContent.previousElementSibling.setAttribute('aria-expanded', 'false');
                    otherContent.previousElementSibling.querySelector('svg').classList.remove('rotate-180');
                }
            });

            // Toggle the state of the clicked accordion
            button.setAttribute('aria-expanded', !expanded);
            if (!expanded) {
                content.style.maxHeight = content.scrollHeight + 'px';
            } else {
                content.style.maxHeight = 0;
            }

            // Toggle the icon rotation for the clicked accordion
            const icon = button.querySelector('svg');
            icon.classList.toggle('rotate-180');
        });
    });
</script>
