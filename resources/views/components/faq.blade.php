<script>
    document.querySelectorAll('.faq-accordion-button').forEach(button => {
        button.addEventListener('click', () => {
            const content = document.querySelector(button.getAttribute('data-target'));
            const expanded = button.getAttribute('aria-expanded') === 'true';
    
            // Tutup semua accordion yang lain
            document.querySelectorAll('.accordion-content').forEach(otherContent => {
                if (otherContent !== content) {
                    otherContent.style.maxHeight = 0;
                    otherContent.previousElementSibling.setAttribute('aria-expanded', 'false');
                    otherContent.previousElementSibling.querySelector('[data-accordion-icon]').classList.remove('rotate-180');
                }
            });
    
            // Toggle accordion yang di-klik
            button.setAttribute('aria-expanded', !expanded);
            if (!expanded) {
                content.style.maxHeight = content.scrollHeight + 'px';
            } else {
                content.style.maxHeight = 0;
            }
    
            // Toggle rotasi icon
            const icon = button.querySelector('[data-accordion-icon]');
            icon.classList.toggle('rotate-180');
        });
    });
    
</script>
    